    public class BoxListener extends MouseAdapter
    {
    	public void mouseClicked(MouseEvent me)
    	{
              JPanel clickedBox =(JPanel)me.getSource(); // get the reference to the box that was clicked 
              if (clickedBox == departurePanel) {
                  selectedStation = departureStation;
              }
              
              if (clickedBox == lastPanel) {
                  
               selectedStation = previousStation;
              }
              if (clickedBox == nextPanel){
                  
               selectedStation = nextStation;
              }
              if (clickedBox == arrivePanel){
                  
               selectedStation = arrivalStation;
              }

         update();
        }
    }

