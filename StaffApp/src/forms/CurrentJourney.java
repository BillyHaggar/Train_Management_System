    private void getJourney() {
        sim.controller.getAPIData();
        
       for(Route route : sim.controller.getRoutes()) {
           if (sim.controller.getSelectedJourney().getRoute_ID().compareTo(route.getRoute_ID()) == 0)
           {
              if (route.getStop_Number().compareTo(1.0) == 0) {
                  for(Station station : sim.controller.getStations()) {
                      if (route.getStation_ID().compareTo(station.getStation_ID()) == 0) 
                          departureStation = station;
                  }
           }
              
                if (route.getStop_Number().compareTo(2.0) == 0) {
                  for(Station station : sim.controller.getStations()) {
                      if (route.getStation_ID().compareTo(station.getStation_ID()) == 0) 
                          previousStation = station;
                  }
           }
                
                if (route.getStop_Number().compareTo(3.0) == 0) {
                  for(Station station : sim.controller.getStations()) {
                      if (route.getStation_ID().compareTo(station.getStation_ID()) == 0) 
                          nextStation = station;
                  }
           }
                
                if (route.getStop_Number().compareTo(4.0) == 0) {
                  for(Station station : sim.controller.getStations()) {
                      if (route.getStation_ID().compareTo(station.getStation_ID()) == 0) 
                          arrivalStation = station;
                  }
           }
           }
       } 
       selectedStation = departureStation;
       
       
    }
    
